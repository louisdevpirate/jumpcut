// Fonction pour créer un film avec des données par défaut
function createFilm(title, index) {
  return {
    id: index + 1,
    tmdbId: null, // Sera rempli après recherche TMDb
    title: title,
    year: null, // Sera rempli après recherche TMDb
    myRating: 0, // À noter par l'utilisateur
    positives: "",
    negatives: "",
    myReview: "",
    dateWatched: new Date().toISOString().split('T')[0] // Date d'aujourd'hui par défaut
  };
}

